// --- Sledov√°n√≠ zmƒõn DOM ---
function observeButtonChanges() {
    const observer = new MutationObserver((mutations) => {
        let needsReapply = false;
        
        mutations.forEach((mutation) => {
            if (mutation.type === 'childList') {
                mutation.addedNodes.forEach((node) => {
                    if (node.nodeType === 1 && node.id && BUTTON_CONFIG[node.id]) {
                        needsReapply = true;
                    }
                });
            }
        });
        
        if (needsReapply) {
            setTimeout(applyButtonVisibility, 100);
            if (DEBUG_BUTTON_VISIBILITY) console.log("ButtonVisibility: Nov√° tlaƒç√≠tka detekov√°na, aplikuji viditelnost.");
        }
    });
    
    observer.observe(document.body, {
        childList: true,
        subtree: true
    });
    
    if (DEBUG_BUTTON_VISIBILITY) console.log("ButtonVisibility: DOM observer aktivov√°n.");
}

// --- P≈ôid√°n√≠ kontextov√©ho menu pro rychl√© ovl√°d√°n√≠ ---
function addQuickContextMenu() {
    // Vytvo≈ôen√≠ kontextov√©ho menu pro prav√© kliknut√≠ na tlaƒç√≠tka
    document.addEventListener('contextmenu', (e) => {
        const target = e.target.closest('button');
        if (target && target.id && BUTTON_CONFIG[target.id]) {
            e.preventDefault();
            
            const contextMenu = document.createElement('div');
            contextMenu.className = 'button-context-menu';
            contextMenu.style.cssText = `
                position: fixed;
                top: ${e.clientY}px;
                left: ${e.clientX}px;
                background: linear-gradient(135deg, #1a1a2e 0%, #16213e 100%);
                border: 2px solid #ff6b35;
                border-radius: 8px;
                padding: 8px 0;
                z-index: 12000;
                box-shadow: 0 10px 30px rgba(0, 0, 0, 0.5);
                min-width: 200px;
            `;
            
            const buttonConfig = BUTTON_CONFIG[target.id];
            const isVisible = buttonVisibility[target.id] !== false;
            
            contextMenu.innerHTML = `
                <div style="padding: 8px 12px; color: #ff6b35; font-weight: bold; border-bottom: 1px solid rgba(255, 107, 53, 0.3);">
                    ${buttonConfig.name}
                </div>
                <div class="context-menu-item" data-action="toggle">
                    ${isVisible ? 'üö´ Skr√Ωt tlaƒç√≠tko' : 'üëÅÔ∏è Zobrazit tlaƒç√≠tko'}
                </div>
                <div class="context-menu-item" data-action="manage">
                    üéõÔ∏è Spr√°va v≈°ech tlaƒç√≠tek
                </div>
                <div class="context-menu-item" data-action="reset">
                    ‚Ü©Ô∏è Obnovit v√Ωchoz√≠
                </div>
            `;
            
            // Styling pro polo≈æky menu
            const style = document.createElement('style');
            style.textContent = `
                .context-menu-item {
                    padding: 8px 12px;
                    color: white;
                    cursor: pointer;
                    transition: background 0.2s;
                }
                .context-menu-item:hover {
                    background: rgba(255, 107, 53, 0.2);
                }
            `;
            document.head.appendChild(style);
            
            // Event listeners pro polo≈æky menu
            contextMenu.querySelectorAll('.context-menu-item').forEach(item => {
                item.addEventListener('click', (e) => {
                    const action = e.target.dataset.action;
                    
                    switch (action) {
                        case 'toggle':
                            buttonVisibility[target.id] = !isVisible;
                            saveButtonVisibility();
                            applyButtonVisibility();
                            window.showNotification(`Tlaƒç√≠tko ${buttonConfig.name} ${!isVisible ? 'zobrazeno' : 'skryto'}`, 'info');
                            break;
                        case 'manage':
                            openVisibilityManager();
                            break;
                        case 'reset':
                            buttonVisibility = { ...DEFAULT_VISIBILITY };
                            saveButtonVisibility();
                            applyButtonVisibility();
                            window.showNotification('Viditelnost tlaƒç√≠tek obnovena na v√Ωchoz√≠', 'info');
                            break;
                    }
                    
                    contextMenu.remove();
                });
            });
            
            document.body.appendChild(contextMenu);
            
            // Zav≈ôen√≠ menu p≈ôi kliknut√≠ jinam
            const closeMenu = (e) => {
                if (!contextMenu.contains(e.target)) {
                    contextMenu.remove();
                    document.removeEventListener('click', closeMenu);
                }
            };
            setTimeout(() => document.addEventListener('click', closeMenu), 10);
        }
    });
}

// --- Export/Import konfigurace ---
function exportVisibilityConfig() {
    const config = {
        buttonVisibility,
        timestamp: new Date().toISOString(),
        version: '1.0'
    };
    
    const blob = new Blob([JSON.stringify(config, null, 2)], { type: 'application/json' });
    const url = URL.createObjectURL(blob);
    
    const a = document.createElement('a');
    a.href = url;
    a.download = 'button_visibility_config.json';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
    
    window.showNotification('Konfigurace viditelnosti exportov√°na!', 'info');
}

function importVisibilityConfig(file) {
    const reader = new FileReader();
    reader.onload = function(e) {
        try {
            const config = JSON.parse(e.target.result);
            if (config.buttonVisibility) {
                buttonVisibility = { ...DEFAULT_VISIBILITY, ...config.buttonVisibility };
                saveButtonVisibility();
                applyButtonVisibility();
                window.showNotification('Konfigurace viditelnosti importov√°na!', 'info');
            } else {
                throw new Error('Neplatn√Ω form√°t konfigurace');
            }
        } catch (error) {
            window.showNotification('Chyba p≈ôi importu konfigurace: ' + error.message, 'error');
        }
    };
    reader.readAsText(file);
}

